package day05_primitives_concatanation;

public class JobInfo {
    public static void main(String[] args) {
        String title = "SDET";
        String company = "Cybertek";
        String jobDescription = "Tester";
        double salary = 125000;
        byte yearsExperience = 10;
        boolean hasBenefits = true;
        System.out.println("Title:\t\t\t\t\t " + title);
        System.out.println("Company:\t\t\t\t " + company);
        System.out.println("JobDescription:\t\t\t " + jobDescription);
        System.out.println("YearsExperience:\t\t " + yearsExperience);
        System.out.println("hasBenefits:\t\t\t " + true);
        System.out.println("Salary: \t\t\t\t $" + salary);

    }
}
