package day04_variables_intro;

public class DeclarationTypes {
    public static void main(String[] args) {
        //declaration + assignment in single statement:
        int bananas = 34;
        System.out.println(bananas);
        int students = 400;
        System.out.println(students);
        students = 400;
        System.out.println(students);

        int peopleInCity, birdsOnTree, fishInLake;
        int PeopleInCity = 1000;
        birdsOnTree = 21;
        fishInLake = 100;

        System.out.println(PeopleInCity);
        System.out.println(birdsOnTree);
        System.out.println(fishInLake);

        int studentsInNY = 58, studentsInVA = 200;

        System.out.println(studentsInNY);
        System.out.println(studentsInVA);

        int soldersInAfghanistan = 1000, soldersInIraq = 2000;
        System.out.println("soldirs in Afghanistan = " + soldersInAfghanistan);
        System.out.println(soldersInIraq);

        int dogs = 20, deer =10;
        System.out.println("Dogs = " + dogs);
        System.out.println("Deer = " + deer);

        int chocolate = 10;
        int sweets = 10;
        System.out.print("chocolate = " + chocolate);
        System.out.print("sweets = " + sweets);







    }
}
