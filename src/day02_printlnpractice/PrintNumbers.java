package day02_printlnpractice;

public class PrintNumbers {
    public static void main(String[] args) {
        System.out.println(100);
        System.out.println(5.1);
        System.out.println("1234,234");
        System.out.println("1234.234");  // it will print as a character not as a value because of double quote.
        System.out.println("123456");
        System.out.println("4567891");
    }
}
